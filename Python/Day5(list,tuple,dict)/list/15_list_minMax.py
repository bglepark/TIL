# 리스트의 값 중 최대값 / 최소값 반환

scores = [90 , 78 , 81 , 64 , 99]
print(min(scores))
print(max(scores))

chr = ['b' , 'A' , 'e' , 'C']
print('최대값 :' , max(chr))
print('최소값 : ' , min(chr))


x = ['banana' ,'apple', 'Coconut' , 'Melon']
print('정렬 : ' , sorted(x))
print('최대값 :' , max(x))
print('최소값 : ' , min(x))

